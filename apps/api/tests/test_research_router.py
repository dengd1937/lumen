# ULID 26-char format: Crockford Base32 (0-9 and A-Z excluding I, L, O, U)
_ULID_RE = re.compile(r"^[0-9A-HJKMNP-TV-Z]{26}$")


@pytest.fixture(autouse=True)
def clear_idempotency_cache() -> Iterator[None]:
    """Clear module-level idempotency cache and lock pool before/after each test.

    Both _IDEMPOTENCY_CACHE and _IDEMPOTENCY_LOCKS must be cleared together so
    tests that monkeypatch the cache do not leave stale locks referencing the old
    TTLCache instance (which would prevent the TOCTOU lock from being acquired).
    """
    from app.routers import research as research_mod

    research_mod._IDEMPOTENCY_CACHE.clear()
    research_mod._IDEMPOTENCY_LOCKS.clear()
    yield
    research_mod._IDEMPOTENCY_CACHE.clear()
    research_mod._IDEMPOTENCY_LOCKS.clear()


