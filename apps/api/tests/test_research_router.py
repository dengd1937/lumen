async def test_session_manager_start_session_passes_query_to_db(
    initialized_db: Path,
) -> None:
    """start_session(session_id=..., query=...) should persist query to DB."""
    import asyncio

    import aiosqlite

    from app.services.langgraph_service import LangGraphStub
    from app.services.session_manager import SessionManager

    sm = SessionManager(db_path=str(initialized_db), langgraph=LangGraphStub())
    await sm.start_session(session_id="q-test-1", query="test query content")

    async with aiosqlite.connect(str(initialized_db)) as c:
        cursor = await c.execute(
            "SELECT query FROM lumen_research_sessions WHERE id = ?",
            ("q-test-1",),
        )
        row = await cursor.fetchone()

    assert row is not None
    assert row[0] == "test query content", f"DB query column mismatch: {row[0]!r}"

    # Wait for task completion to avoid pending tasks after test ends
    if "q-test-1" in sm.active_runs:
        await asyncio.wait_for(sm.active_runs["q-test-1"], timeout=2.0)
        await asyncio.sleep(0.05)


def test_session_manager_start_session_requires_keyword_args() -> None:
    """start_session signature enforces keyword-only (*) - positional call should TypeError."""
    import inspect

    from app.services.session_manager import SessionManager

    sig = inspect.signature(SessionManager.start_session)
    params = list(sig.parameters.values())

    # Skip self; remaining params should all be KEYWORD_ONLY
    non_self = [p for p in params if p.name != "self"]
    for p in non_self:
        assert p.kind == inspect.Parameter.KEYWORD_ONLY, (
            f"param {p.name!r} should be KEYWORD_ONLY, got: {p.kind}"
        )
