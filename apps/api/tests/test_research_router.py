def test_start_session_idempotency_returns_same_session_id_within_window(
    env_settings: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    """Same client_request_id within 60s window -> returns same session_id; second call 200 not 201."""
    from app.routers import research as research_mod
    from main import app

    # Use a longer TTL to ensure window does not expire during test
    monkeypatch.setattr(
        research_mod,
        "_IDEMPOTENCY_CACHE",
        TTLCache(maxsize=1024, ttl=60.0),
    )

    crid = "test-idem-001"
    with TestClient(app) as client:
        r1 = client.post(
            "/api/research/start",
            json={"query": "idempotency test", "client_request_id": crid},
        )
        assert r1.status_code == 201
        sid1 = r1.json()["session_id"]

        r2 = client.post(
            "/api/research/start",
            json={"query": "idempotency test", "client_request_id": crid},
        )
    assert r2.status_code == 200
    sid2 = r2.json()["session_id"]
    assert sid1 == sid2, f"idempotency hit should return same session_id, but {sid1!r} != {sid2!r}"


def test_start_session_idempotency_outside_window_creates_new_session(
    env_settings: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    """After TTL expiry, same client_request_id creates a new session_id."""
    from app.routers import research as research_mod
    from main import app

    # TTL=0.1s so cache expires quickly
    monkeypatch.setattr(
        research_mod,
        "_IDEMPOTENCY_CACHE",
        TTLCache(maxsize=1024, ttl=0.1),
    )

    crid = "test-idem-002"
    with TestClient(app) as client:
        r1 = client.post(
            "/api/research/start",
            json={"query": "expiry idempotency test", "client_request_id": crid},
        )
        assert r1.status_code == 201
        sid1 = r1.json()["session_id"]

        # Wait for TTL to expire
        time.sleep(0.2)

        r2 = client.post(
            "/api/research/start",
            json={"query": "expiry idempotency test", "client_request_id": crid},
        )
    assert r2.status_code == 201
    sid2 = r2.json()["session_id"]
    assert sid1 != sid2, f"after TTL expiry should create new session_id, but both: {sid1!r}"


